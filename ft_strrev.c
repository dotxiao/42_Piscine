char	*ft_strrev(char *str)
{
	int		i;
	int		l;
	char	tmp;

	l = 0;
	while (str[l] != '\0')
	{
		l++;
	}
	i = -1;
	while (++i < --l)
	{
		tmp = str[i];
		str[i] = str[l];
		str[l] = tmp;
	}
	return (str);
}
